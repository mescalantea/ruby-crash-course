# Data types are:
# 1. Numbers
# 2. Strings
# 3. Booleans
# 4. Arrays
# 5. Hashes
# 6. Symbols
# 7. Nil

# Numbers
# - Integers
my_integer = 25
puts "my_integer = " + my_integer.to_s

# - Floats
my_float = 3.14
puts "my_float = " + my_float.to_s

# Strings
my_string = "Hello world"
puts "my_string = " + my_string

# Booleans
my_boolean = true
puts "my_boolean = " + my_boolean.to_s

# Arrays
my_array = [1, 2, 3, 4, 5]
puts "my_array = " + my_array.to_s

# Hashes
my_hash = {name: "John", age: 25, city: "New York"}
puts "my_hash = " + my_hash.to_s

# Symbols
my_symbol = :my_symbol
puts "my_symbol = " + my_symbol.to_s

# Nil
my_nil = nil
puts "my_nil = " + my_nil.to_s
