# Raise a number to the power of another number
puts 2**3 # This will output 8 that is 2^3

# Division remainder
puts 10 % 3 # This will output 1 that is the remainder of the division 10/3

num = 1.4

# Convert a float to an integer
puts num.to_i # This will output 1

# Round a float
puts num.round() # This will output 1

# Round a float up
puts num.ceil() # This will output 2

# Round a float down
puts num.floor() # This will output 1

# Math functions
# - Square root
puts Math.sqrt(36) # This will output 6

# - Logarithm
puts Math.log(1) # This will output 0.0
